/**
 * Base configuration options for a generic page module.
 *
 * Defines the Module Federation references required to resolve and render
 * a page within the host application.
 */
export interface ModulePageOptions {
  /**
   * Module Federation remote/component used to resolve the layout.
   *
   * This value points to an exposed module (remote entry + exposed path)
   * that provides the layout implementation to be loaded by the host.
   */
  layout: string;

  /**
   * Module Federation remote/component used to resolve the page.
   *
   * This value points to an exposed module (remote entry + exposed path)
   * that provides the page implementation to be loaded by the host.
   */
  page: string;

  /**
   * Route path associated with the page.
   *
   * Defines the URL at which the page will be mounted in the host routing
   * system.
   */
  pagePath: string;
}
